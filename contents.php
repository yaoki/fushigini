<p>　目が合ったフミカちゃんは、きらりと身をおどらせて私の世界へ躍り込んだ。</p>

<p>　この子の鋭い距離感覚にほんとうに感心する。</p>

<p>　私の親しみを感じる領域に入ったフミカちゃんは、何も言わずに、私と、私の後方に広がっているはずの非常に広い景色とを、漠然と見比べながら、後ろ歩きなのを苦にすることなく、むしろ楽しそうな足取りで、私の少し先を歩いた。風に吹きつけられて背中が押される。私はその女の子に声を掛けた。</p>

