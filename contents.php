<p>　私たちが呼ばれた理由は言うまでもなかった。</p>

<p>「試験勉強？」と私たちは尋ねた。彼女たちはどこでするのだろうか。</p>

<p>「一緒にしようよ！」</p>

<p>といくつかの声が揃って返ってきた。たぶん教えるのが成績のいい私と杏子ちゃんの役割になるんだけど、試験のときぐらい彼女たちとお付き合いをしてもいい。嫌われてるわけではないし、好かれていないわけではないし、ちょっとしたイベントのときだけ仲良くしておくことは関係としてそれなりに心地よいものなので。</p>

