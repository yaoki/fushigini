<h2>11フシギにステキな素早いヤバさ</h2>

<p>　夕方の首里の街は私の知っている世界で一番さまざまな音声情報に満ちあふれている場所に思えた。写真屋さんの看板や八百屋さんの店頭に並べられた野菜が黄昏のくらさに視覚情報を失っていき、誰のともわからないたくさんの声と噪音が私の立っている周囲のあちこちからランダムに聞こえてくる。</p>

<p>　バス停で降ろされてバスを待つ間の暇さが、眠気だけを私に思い出させる。ノートを取り出してみる。ドット入りキャンパスノートの表紙のしなやかな硬さが、水銀灯の下に立っている私のシルエットと一体をなして、首里のアスファルトの舗道に映って動いているのを見て、文学的だと思う。その緑色が。</p>

<p>　フルスクリーンで</p>

<p>　私の路上歩行</p>

<p>　なんて。</p>

<p>　歌詞の冒頭を見て小さく吹き出した私の笑い声の、その向こうでは、学校帰りの中高生たちや、買物途中の中高年たちや、遊びに繰り出す地元の若者たちや、首里の観光に来ている観光客たちの話す声、イヌやネコの声などが耳を澄ますとひっきりなしに聞こえた。それらはとてもうるさかった。行き交う自動車たちと空をゆくヘリコプターと飛行機たちもうるさかった。私は、様々な音に共鳴して揺れる、水銀灯の下の、バス停の固い板にもたれかかった。</p>

<h2>12フシギにステキな素早いヤバさ</h2>

<p>　心は遠くはずむもの。体は重く地面へ引かれる。</p>

<p>　目覚まし時計が鳴る時間は朝。四月の街は犬がいっぱい。</p>

<p>　連続する平然とした気持ち。赤外線で道路が温かい。</p>

<p>　夜の道路。空腹の私たち。</p>

<p>　面倒な青春。居場所のないゴールキーパー。</p>

<p>　食堂にならぶ高校生たち。クリップの跡がついた朝自習のプリント。</p>

<p>　駅前の大人たち。眠気を誘う鳩の群れ。</p>

<p>　電車の中吊り広告の赤色。ブラウスを透かして見えている日焼け跡。</p>

<p>　ため息のつきかた、嘘のつきかた、夢の見方、君の味方。高い場所から見える景色。</p>

<p>　城の石垣に腰かけて、白いスケーターシューズが揺れるのを、私は見ていた。私が３週間前に作った歌を口ずさんでいるフミカちゃんの澄んだ声が、透明な布のようによるの町並みの上を飛んでいく気がする。</p>

<p>「ねえ、私の歌あってるかな。私が歌ってもいい歌詞なのかな」</p>

<p>　フミカちゃんは、豆粒のようにちいさく点灯している信号機が赤に変わると、私の方に顔を寄せてそう聞いた。</p>

<p>　二人の間には乾いた風が音を立てて吹く。</p>

<p>「いい歌詞はね、誰が歌っても心地いいんだよ」、</p>

<p>なんて、そんなふうに私は答えた。パンくずが風に舞って、首里の夜空を通り過ぎていく。フミカちゃんが、「はいこれ」と言って半分くれたアンパンの袋が飛ばないように、私は慌ててそれをポケットへ突っこんだ。そしてポケットの中は温かい。</p>

<p>　フミカちゃんは私の目を見つめる。</p>

<p>「たとえば、今日のあなたの家の夕食がコロッケだったとするじゃない。そしたら私の家の夕食には何が出てると思う？」</p>

<p>　昼の熱を持った、生温かい石垣の城壁の上に、垂らしていた両足を載せ、膝を抱えて上目づかいで聞くフミカちゃん。このクイズはどんな意味があるのだろうか。フミカちゃん家の夕食？</p>

<p>　そんなのわからないけどなあ。ちょっと想像してみる。</p>

<p>「考えてみて」とフミカちゃんが言う。</p>

<p>　あのあたりがフミカちゃんの家のあるところだ。遠くなので、どの灯りがフミカちゃんの家なのかわからないけど、オレンジ色の光のあたりになんとなくあたりをつける。今、あの家に帰ってドアを開けるとどんな料理のにおいがするだろうか。</p>

<p>　私の家の夕食にコロッケが出たとしたら、私にはその匂いを想像することはできる。熱いご飯が盛られた上に、スーパーから買ってきた冷たいコロッケが載っているのを。私専用の朱塗りの箸でそれを裏がえして、両面がほどよく温まるまで待つのだ。</p>

<p>　私が夕食を食べているのと同じ時間に、フミカちゃんは自分の席に着いて何を食べているだろうか。</p>

<p>　私の返答を待つために、夜の街を見下ろしているフミカちゃんの横顔は素敵だった。路上を動く、車や信号や歩行者や犬や木々に、愉快そうに目を踊らせていた。</p>

<p>「コロッケじゃないかな」、</p>

<p>と私はつぶやいて、城の石垣の上に立ち上がった。しゃがんでいたときの視線に、私の背の高さがプラスされ視界が広がるのがわかった。</p>

<p>　フミカちゃんは何も言わずにうなずいた。フミカちゃんの腕に付けていたひものようなアクセサリーがつられて揺れた。</p>

<p>　大きく、大きく、私が３週間前に作った歌が、首里の城壁の上でフミカちゃんによって歌われた。少し遠くに立っている私にもはっきりと歌詞が伝わるように、フミカちゃんは歌った。</p>

<h2>13フシギにステキな素早いヤバさ</h2>

<p>　眠くてたまらないということが自分でもわかる。</p>

<p>　真夜中、それは私にとって12時を過ぎた時間のことだ。つまり、午前零時。風が少しずつ冷たくなるのにつれて、私の心も落ち着いてくる。</p>

<p>　生きていることなんてとてもつまらないしくだらないし、夜空の一億二千光年の遠くから届いてくる星たちの呼吸をするような瞬きの美しさに比べたらほとんど無意味だ。私がわざわざ奮闘してバカみたいな苦しい気持ちで生きつづけていても、誰にとっての何の足しにもならないだろう。</p>

<p>　洗濯物を干すために、部屋の蛍光灯を消してスリッパを履き、ベランダへ出る。</p>

<p>　星がいっぱいだ。。その星空の下には、星の数と同じくらいの墓標たちがある。一基につき４・柱までが収められるのだが、この霊園はまだ開園して２年たらずなので、中には１柱のみしか収められていないのも多い。背の高いキビ科の植物たちが、青く照らされて鬱蒼と揺らめいているのがわかる。あの暗い根元では、鳥たちが寝ているのかもしれない。</p>

<p>　空気が濁って苦い。口の中にしみ込む透明な吐き気が痛みに似ているようだ。</p>

