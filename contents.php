<h2>８フシギにステキな素早いヤバさ</h2>

<p>　時計は自分のガラス板をつややかに光らせていて、そこに午後５時のオレンジ色の日差しが差していた。時計の下半分を斜めに切るように教室内の風景が、その下に続いて広がっていた。だから、時計の上半分だけ、窓の外の赤色の夕景色と同じ世界にあるみたいに見えた。</p>

<p>　放課後だ。</p>

<p>　みんなが帰った後も、この貴重な景色を観察するために自分の席にバカみたいに座りつづけていた私は、日没後の目の中に残るめまいの感覚を、緑色の視界の奥にぼやけて見える黒板を眺めながら楽しんでいた。膝をちょっと開いてつま先を交互に動かしてみる。いつもとは違うメロディーが聞こえてきそうだ。いつもとは違うリズム。</p>

<p>　先程、私のところに男子と女子が来て、東京部という部活への勧誘をしていったのだった。別に正規の部活ではない。数学教師にも勧められたように、私の学力がなかなかのものなので、一緒に東京を目指したいということだった。今、机の上に反射している蛍光灯の光に目を落としている私は耳にまだその言葉を思い出すことができる。</p>

<p>　私はとりあえず入部することにしたのだった。私は何か私の物語を始めなければならなかったし、知らない先輩や後輩とふれあう部活動というものに自分の心をひかれたからでもあった。私の頭の中から、東京をめざす東京部というコンセプトのおかしさ加減への違和感を、外へ振りはらっておいた。</p>

<p>　部長らしい女の先輩の口頭での勧誘惹句によれば、みんなで勉強したり、小説を書いたり、俳句を書いたり詩を書いたりして東京へのモチベーションを上げ、上京を目指す、というのがこの部のコンセプトらしかった。</p>

<p>　東京。</p>

<p>　東京なのだ。</p>

<p>　たしかにそこは魅力的な場所であることが想像される。私は机の中から手探りで電子辞書を引っ張り出して大辞林第三版を引いてみる。</p>

<p>「み　りょく［０］【魅力】人の心をこころよく引きつける力。『音楽の――』」</p>

<p>　私はメモするために立ち上がって、床の上に散らばっている朝自習のプリントを一枚拾ってもう一度いすに座り、今の内容をそこへメモしておく。心を動かされたものはこうしてメモしておく必要がある。そして忘れないように、教室の壁に押しピンで貼っておく。代わりに一枚の掲示物を剥がしておいた。</p>

<p>　私はいつもしっかりした自覚を持たないで生きている。</p>

<p>　石積みでできた中城城跡の石垣をのぼるときみたいに、物語の手がかりに私の指先を引っかけて、何かに向かって生きるべきなのかも知れない。私も物語を形成しなければならないと思った。</p>

<p>　物語の成立はたぶん、主人公が物語を生きたいと願う意志の存在によって定まると思う。これは私の仮説だ。</p>

<p>　仮説は実践して検証してみるしかない。</p>

<p>　だから、物語の発端を探しつづけるのではなく、私は私の物語を生きるという意識を持たなければ、物語は論理的に成立しえないのである。</p>

<p>　私は、端から見ると、教室の中で天井を見上げたり窓の方を向いたりしながらずーっとぼうっとしている人間だろう。</p>

<p>　スリッパをちゃんと履きなおして、私は教室と廊下を出たり入ったりしてみる。変わりのないこの世界だけど、私が何とかして変えるしかないのだろう。歌を歌ってみる。</p>

<p>　廊下に出て、廊下の特有な音の反響の仕方を楽しみながら、私は、私の口の中に自然に生まれてくる言葉の組みあわせたちに適当なストーリーを持たせるメロディーを関連づけて、それらを歌へと変えてゆく。</p>

<p>　歌を作ることが私にとってはこんなにもカジュアルでカンタンなのに、物語を作るということが私にとってどれほど難しいことだろうか。</p>

<p>　誰もいない教室の中に引きかえして、偽物のプラスチックみたいな掃除用具入れの戸を開き、私のエレクトリックギターを持ってきた。</p>

<p>　エレクトリックギターは重い。右肩のあたりが疲れるので、あまり長い時間は弾いていられないのがいつも悲しい。緑色の私のスリッパが床に擦れて鳴る音に似た悲しさだ。</p>

<p>　廊下の白亜の壁に私の肩と体をもたせ、私の頭の優れた言語処理野と音楽処理野が生みだす歌詞たちとメロディーたちとを、私という独立した人格がコンポーザーとして、曲のイメージを統率しながら生演奏していった。エレクトリックギターの弦の金属音には豊かなアタック音がたくさん含まれている。コードの響きよりも私の耳はそれらの立てる複雑に入れ替わっていくリズムをうっとりと聞く。ついでに私は歌を歌った。でまかせの歌詞に適当なメロディをつけて、ポップな感覚を求めて歌った。誰にも届かなくても、自分の耳には届いている。自分の耳には、自分の好きな歌を歌って聞かせてあげたかった。どきどきするような日々じゃないのに、風はいい風だった。</p>

<p>　誰も聞いていないストリートライブ。</p>

<p>　誰も見ていないスクールライフ。</p>

<p>　東京でたとえると、さしずめ、高円寺駅前の弾き語りミュージシャンといったところだろうか。</p>

<p>　遠くから私を見ていたら、弁ヶ嶽の草木たちや私の教室の掲示物たちや各家庭の洗濯物たちが、夕方の大気の流れに合わせて揺れている中で、肩までの髪を揺らせ、少し横を見て、口先とつま先を動かし、全体的には、自分でリズムを決めて律動しているもののように見えただろう。アスファルトの上を柔らかい肉球で音を立てないように四足歩行する猫とは違うし、南洋の空に枝をめいっぱい伸ばしてスモモ色の丸い実をたくさんに実らせるクワ科の樹木とも違っている。どちらかといえば、下北沢の電車が走るトンネルの下で少年ジャンプの朗読をしている芸人や、秋葉原駅の駅前でバンド演奏をしているミュージシャンたちに近いと思う。</p>

<p>　廊下の壁に私が貼った３８枚のＡ４プリント用紙の裏に書かれた歌詞とコード。これは毎月の発表会のために私が書き下ろしてきた曲の数々で、多くはない。</p>

<p>　学校はとてもつまらないので、担任にお願いして月に一回、ホームルームの時間を借りて歌を発表している。歌がうまいわけでもないし、ギターがうまいわけでもないけど、私の歌をみんなは聴いてくれる。人気のよかった歌の歌詞とコードは後でパソコンに打ち込んでプリントアウトして壁に貼っておくようにしていた。このクラスには新聞委員も掲示係もいないので、教室の壁は、私の高校が発行している毎日の進路新聞や、音楽科と美術科の定期演奏会や展覧会のお知らせをのぞけば、ほとんど私の作った時間割表や、私のメモが書かれたプリントの裏や、私の詩が書かれたノートの切れ端や、私の作ったマンガなどで埋まっていた。邪魔なら先生か生徒のうちの誰かが剥がすだろうと思っていたが、みんなとくに何とも思わずに過ごしているようだ。それで、剥がされていない。</p>

<p>　ライブハウスに行けば、真っ黒に塗られたコンクリート壁に、大量のライブ告知ビラたちがびっしりと貼られているが、そういうものと思えば気にならない。ひとつのバンドの演奏が終わって次のバンドの入れ替えまでの１０分ぐらいの時間に、見るともなくそれらのビラたちを眺める。</p>

<p>　そんな感じだ。</p>

<p>　そういえば、私のある歌がヒットして、２週間ぐらいの間、男子たちが私のギターを借りて毎日歌っていることがあったのはちょっと心を動かされた。</p>

<p>　私の作った歌も人の口にのぼることがあるのだなあと思った。</p>

<p>　今、弁ヶ嶽の木々の揺れるのを眺めながら私の口ずさんでいる歌は、ギターとアンプをつなぐシールドが抜けて、女の子の足首に絡まる、という歌詞だった。私は自分でも、そのような歌詞を作る自分の才能を素敵だと思う。</p>

<h2>９フシギにステキな素早いヤバさ</h2>

<p>　見聞きしたすべての事物には大概それに対応した概念つまり言葉があるというのを、それなりに賢いので旧帝大に行ったりする私の高校の同級生や先輩たちは知っていて、高校の近隣にある弁ヶ嶽という高いところへ明鏡国語辞典を携えて連れ立っていき、そこから見える首里のすべての、ほんとうに凝集した建築や人物たちに対応した言葉を、賢い誰がもっとも早く見つけられるか、競争しに、冗談交じりに向かったりしていた。</p>

<p>　フミカちゃんも、そのような知的遊戯心にみちた無邪気でフシギな生徒のひとりだった。そして、見かけは誰にも負けないぐらいの、少しずつ昼の太陽の紫外線にさらされて日灼けするだけの体操服のひとりの女子高生のようでも、彼女の視線は、世界中にちらばった彼女の語彙の中から、魔法をつかうためにぴったりの言葉を検索しているに違いなかった。私の知る世界ではそのような努力を怠る大人たちは多かった。</p>

<p>　フミカちゃんの魔法はよく世界を色とりどりに変えた。</p>

<p>　隣を歩く、発熱する物体は、考え、歌いながら様々な言葉たちを私の中へ次々に投げ込んできた。歩くにつれて、そのリズムに合わせたフミカちゃんの想像がふくらんでは言葉という形となって空へと消えていくのを、私は今でもスケッチブックをもって全て書きとめて置きたかったと思うぐらいだ。</p>

<p>　私はそのスケッチブックを抱きしめてお棺に入るだろう。</p>

<p>　その隣では、数学教師が、誰ともペースを合わせることなく、黙々と天を眺めたり地を眺めたり人を眺めたりして、それらについての自分の考えを一四〇字以内で口頭で述べていた。写メを撮ったりツイートしたりしていた。だからこそ、私たちと越しつ越されつすることで、どことなく透明感のあって、心地よい距離感のあるコミュニケーションを取れていたのかもしれなかった。なんだか首里の街並みからはすべての私の世界の雰囲気を実況するバイオリンのＢＧＭが、聞こえてくるような気さえしたぐらいだ。それが妙に甘かった。</p>

<p>　ところで、舞台を、今私の生きている今現在時に戻す。教室の真ん中に自分の席を作って座り、放課後の誰もいない教室で、自分を幸福にするための音楽を自分のために作ろうと、アンプに繋がれてないエレクトリックギターを私が爪弾いていると、世界史の教師が不自然な歩調でやって来た。体重のかかった静かな足音は私のギターのリズムへの、明らかなバスドラムのリズムを示していたように思えた。</p>

<p>「よう。スカート履いてる？」</p>

<p>　あいにく、授業終了後にショートパンツに履きかえていた私は、つややかに光る、自分のむき出しになった膝小僧を見ながら、頭の中で素早く、彼の挨拶を上回る、なにか面白い返答法はないか検索してみたのに、残念ながら洗練に欠ける私の語彙と経験からは、何にもネタが導きだされなかった。</p>

<p>「おつかれさまです。お茶もお菓子もありませんが、お金も夢もないけど、歌詞ならあります」</p>

<p>　と顔を上げながら、制服のスカートの代わりにショートパンツを履いて、脚を組む、何の変哲もない女子高生に見えるであろう私は、制服の上着にエレクトリックギターを抱えたまま、とても気の利いたことを言った。私が国語の教師なら褒めてあげただろう。</p>

<p>「机の上にＢ５のコクヨのキャンパスノートを広げて、私は、歌詞を書いていたのです。歌を作るために」</p>

<p>　私の説明に促されて、百数冊もの高校生たちの昼間のノートたちと、何種類ものくせのある字体たちとを区別して見分けている筈の世界史の教師の目は、私の机の上の、きれいにふたつに開かれているためにＢ４サイズ相当の広い面積を今は占めている、私のコクヨのドット入りキャンパスノートを、見た。私はすばやく鉛筆を取って、ページの上部余白に対して「歌詞を」と大きく書きこんだ。</p>

<p>「歌詞。歌詞書いてるのか」</p>

<p>　と世界史の教師は言った。</p>

<p>「スカート履かないで歌詞書いているのか」</p>

<p>　スカート履かないでは要らないけど。</p>

<p>　私のノートを手に取ると、私の高校の世界史の教師はたどたどしい声でそれを朗誦した。私は抑揚のない声が好きだった。まるで、私は、孤独ではないようだった。</p>

<h2>10フシギにステキな素早いヤバさ</h2>

<p>　教室の戸締まりをして、校庭へ出ると、粘り気のある物質のような夜の闇が、いくつもの墓たちに囲まれた私の高校には、訪れていたようだった。私は、自分の自意識を研ぎすませ、今、自分が立っているこの場所から、世界中のどんな音が聞こえ、どんな光が見えるのかのそれぞれを、順番に調べようとした。映画やゲームをプレイしているときに、突然、それまでは空気のように存在感を消したままで、私たちのプレイのリズムを奏でていたＢＧＭが、完全に消え去り、かといって、何のイベントも始まる予感のないまま、静寂が訪れたとき、プレイヤーたちは次に何が起こるかに注意しなければならない。一〇〇〇メートルの遠くで、携帯電話が鳴り、ただちにそれがオフされる音が聞こえたような気がした。あとは、ぽっかりと誰もいなくなったグラウンドの周囲に生い繁っている草たちが、マメ科の植物たち特有の、ざらざらした音響を立てるのが、ただ聞こえた。あとは、自分が生きてるって感じがした体温と呼吸とが、私自身の感覚を通して感じられるぐらいだった。</p>

<p>　何か面白いゲームのイベントが起こる前触れのように感じられたのは、逆に言えば、たんに私が、目の前の暗がりの中で佇立している物々たちの輪郭にとらわれていて、放課後の教室で私と一緒に話していた世界史教師と職員室の前で別れたばかりだった、ということを忘れていたからに過ぎなかった。</p>

<p>　私の思考を邪魔するように、いつのまにか、青いセダンが突進してきて、左側の助手席のドアを電車のドアのように自然に開いてくれた。</p>

<p>　暗がりに列をなして浮かんでいる墓標たちを眺めていた私は、私は生きることに淫してはならない、とそこに立つ自分自身に言い聞かせて、車の中にＩＮした。</p>

<p>「ＹＯＵ何してくれるの」</p>

<p>「バス停まで乗せて行って差しあげようと思ったのさ」</p>

<p>「ＭＹ家までじゃないんだ」</p>

<p>「西原町にある君の家までは、さすがに送ることができないんだ。ごめんのごの字に免じて許してほしいんだよね。ごめん」</p>

<p>　彼の車の中は何の匂いもしなくて、それを不思議に思った。音楽もラジオも流れていなかった。</p>

<p>　フロントガラスを見ると、運転の邪魔にならないように配慮されつつ、世界史の年号と事項とが併記された赤や緑のポストイットがたくさん貼られて、きらびやかに揺れていた。そして窓から入る空気が心地よいと思わされた。</p>

<p>「一九一四年　第一次大戦勃発」</p>

<p>　すぐに胸の中へ、真昼の教室で授業を受けている時の甘酸っぱい感覚が放蕩息子のように帰ってくる。</p>

<p>「何のためにここへ貼っているのですか」</p>

<p>「もちろん君のために貼っているのさ。君ならそれを素敵な飾りだと思うだろう？」</p>

